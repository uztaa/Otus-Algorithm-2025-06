#pragma once

#include <vector>

/**
 * @brief Маленький сарай - простой перебор всех прямоугольников
 * 
 * Решает задачу: найти максимальную площадь прямоугольника из нулей
 * для матрицы размера N x M, где N, M <= 30
 * 
 * Алгоритм (брутфорс):
 * 1. Перебираем все возможные левые верхние углы (i1, j1)
 * 2. Перебираем все возможные правые нижние углы (i2, j2)
 * 3. Для каждого прямоугольника проверяем, все ли клетки свободны
 * 4. Отслеживаем максимальную площадь
 * 
 * Временная сложность: O(N^2 * M^2 * N * M) = O(N^3 * M^3)
 * Пространственная сложность: O(1)
 */
class TaskFiveSmallBarn {
public:
    /**
     * @brief Найти максимальную площадь прямоугольника из нулей
     * 
     * @param matrix N x M матрица (0 - свободно, 1 - занято)
     * @return int Максимальная площадь
     */
    static int execute(const std::vector<std::vector<int>>& matrix);

private:
    /**
     * @brief Проверить, все ли клетки в прямоугольнике свободны
     * 
     * @param matrix Матрица
     * @param i1 Верхняя строка
     * @param j1 Левый столбец
     * @param i2 Нижняя строка
     * @param j2 Правый столбец
     * @return bool true если все клетки свободны
     */
    static bool isRectangleEmpty(const std::vector<std::vector<int>>& matrix,
                                 int i1, int j1, int i2, int j2);
};
