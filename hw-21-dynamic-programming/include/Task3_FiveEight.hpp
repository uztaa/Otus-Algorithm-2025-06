#pragma once

/**
 * @brief Найти количество N-значных чисел из цифр {5, 8} без трёх подряд
 * 
 * ДП со состояниями
 * 
 * ДП соотношение:
 * dp[i][d1][d2] = количество i-значных чисел, где последние две цифры d1 и d2
 * 
 * Переход:
 * - Если не три подряд: dp[i][d2][new_digit] += dp[i-1][d1][d2]
 * Временная сложность: O(N)
 * Пространственная сложность: O(1) (фиксированное количество состояний)
 * 
 * @note N может быть до 88
 * @note Используем состояния: 0 - начало, 5 - цифра 5, 8 - цифра 8
 * 
 * @example
 * N=1: "5", "8" → 2
 * N=3: 558, 585, 588, 855, 858, 885 → 6
 */
class TaskTheFiveEight {
public:
    /**
     * @brief Найти количество N-значных чисел из {5, 8} без трёх подряд
     * 
     * @param n Количество цифр (1-88)
     * @return long long Количество допустимых чисел
     */
    static long long execute(int n);

private:
    // Индексы для состояний: 0 - начало, 1 - цифра 5, 2 - цифра 8
    static const int START = 0;
    static const int DIGIT_5 = 1;
    static const int DIGIT_8 = 2;
};
